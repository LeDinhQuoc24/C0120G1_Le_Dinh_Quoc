package Bai_4_Ke_thua.Bai_tap.Lop_Point_MoveablePoint;

public class Point {
    private float x=1.0f;
    private float y=2.0f;
    public Point() {
    }
    public Point(float x,float y) {
        this.x=x;
        this.y=y;
    }
    public void setX(float x) {
        this.x=x;
    }
    public float getX() {
        return this.x;
    }
    public void setY(float y) {
        this.y=y;
    }
    public float getY() {
        return this.y;
    }

}
